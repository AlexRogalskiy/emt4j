package org.eclipse.emt4j.common;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;


/**
 * A rule implementation must contains the annotation.
 * Use the type to match the type that defined in rule.xml.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface RuleImpl {
    String type();

    /**
     * the larger tha lower priority
     *
     * @return
     */
    int priority() default 3;
}
